# NOTES ---
# - The single most important skill for a computer scientist is "problem solving."
# - Algorithms are the solutions we create through the problem solving process.
# - Our goal in computer science is to take a problem and develop an algorithm
#   that can serve as a general solution.
# - High-level languages are programming languages such as Python, C++, PHP, and
#   Java.
# - Low-level languages, sometimes referred to as machine languages or assembly
#   languages, are things such as binary.
# - Computers can only execute programs written in low-level lanuages. So,
#   programs written in high-level languages need to be processed before they can
#   run.
# - Two types of programs process high-level languages into low-level lanugages.
# - Interpreters read a high-level program and executes it, meaning it does what
#   the program says. [Source Code -- Interpreter -- Output]
# - Compilers read the program and translate it completely before the program
#   starts running. [Source Code -- Compiler -- Object Code -- Executer -- Output]
# - A program is a sequence of instructions that specifies how to perform a
#   computation.